// List Interface and its Methods

// The List interface in Java is part of the Collections Framework and represents an ordered collection (also known as a sequence). 
// Lists can contain duplicate elements and provide precise control over where each element is inserted. 
// The primary implementations of the List interface are ArrayList and LinkedList.



public class ListInterfaceExample {

    // List Interface
    // The List interface extends the Collection interface and includes methods for positional access, searching, iteration, and range-view operations.
    // Some commonly used methods are:
    
    // add(E e): Appends the specified element to the end of the list.
    // add(int index, E element): Inserts the specified element at the specified position.
    // get(int index): Returns the element at the specified position.
    // remove(int index): Removes the element at the specified position.
    // set(int index, E element): Replaces the element at the specified position with the specified element.
    // indexOf(Object o): Returns the index of the first occurrence of the specified element.
    // size(): Returns the number of elements in the list.
    // isEmpty(): Returns true if the list contains no elements.

}
